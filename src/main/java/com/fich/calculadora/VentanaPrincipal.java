    private void buttonIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonIgualActionPerformed
        double segundoNum;
        switch (operacion) {
            case "sumar" -> {
                segundoNum = Double.parseDouble(mostrar);
                resultado = primerNum + segundoNum;
                labelResultado.setText(String.format("% .2f", resultado));
                mostrar = String.valueOf(resultado);
                operacion = "null";
            }
            case "restar" -> {
                segundoNum = Double.parseDouble(mostrar);
                resultado = primerNum - segundoNum;
                labelResultado.setText(String.format("% .2f", resultado));
                mostrar = String.valueOf(resultado);
                operacion = "null";
            }
            case "multiplicar" -> {
                segundoNum = Double.parseDouble(mostrar);
                resultado = primerNum * segundoNum;
                labelResultado.setText(String.format("% .2f", resultado));
                mostrar = String.valueOf(resultado);
                operacion = "null";
            }
            case "dividir" -> {
                segundoNum = Double.parseDouble(mostrar);
                if (segundoNum == 0) {
                    labelResultado.setText("Error");
                } else {
                    resultado = primerNum / segundoNum;
                    labelResultado.setText(String.format("% .2f", resultado));
                    mostrar = String.valueOf(resultado);
                    operacion = "null";
                }
            }
            case "null" -> {
                labelResultado.setText(mostrar);
            }
        }

        labelCuentaActual.setText("");
        activarOperacion = true;
        activarPunto = true;

        segundaOperacion = true;

    }//GEN-LAST:event_buttonIgualActionPerformed

